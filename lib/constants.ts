// NORMALIZATION & HELPER FUNCTIONS

// Normalize string untuk perbandingan (lowercase, trim, hapus spasi berlebih)
export function normalizeString(str: string): string {
  return str.trim().toLowerCase().replace(/\s+/g, " ");
}

// Helper function untuk cek apakah kegiatan termasuk dalam gedung
export function isInsideBuildingActivity(title: string): boolean {
  const normalizedTitle = normalizeString(title);
  return INSIDE_BUILDING_ACTIVITIES.some(
    (inside) => normalizeString(inside) === normalizedTitle
  );
}

// Helper function untuk cek apakah lokasi termasuk dalam gedung
export function isInsideBuildingLocation(location: string): boolean {
  if (!location) return false;
  const normalizedLocation = normalizeString(location);
  return INSIDE_BUILDING_LOCATIONS.some(
    (inside) => normalizeString(inside) === normalizedLocation
  );
}

// Helper function untuk filter titles berdasarkan category
export function filterTitlesByCategory(
  titles: string[],
  category: string
): string[] {
  if (!category || category === "all" || category === "") return titles;
  
  return titles.filter((title) => {
    const isInside = isInsideBuildingActivity(title);
    if (category === "inside") return isInside;
    if (category === "outside") return !isInside;
    return true;
  });
}

// Helper function untuk filter locations berdasarkan category
export function filterLocationsByCategory(
  locations: string[],
  category: string
): string[] {
  if (!category || category === "all" || category === "") return locations;
  
  return locations.filter((location) => {
    const isInside = isInsideBuildingLocation(location);
    if (category === "inside") return isInside;
    if (category === "outside") return !isInside;
    return true;
  });
}

// AUTO-SUGGEST LOKASI BERDASARKAN NAMA KEGIATAN

/**
 * Mendapatkan lokasi default berdasarkan nama kegiatan.
 * Mengembalikan string lokasi jika ada pola yang cocok, atau null jika tidak ada pola.
 */
export function getDefaultLocationForActivity(title: string): string | null {
  if (!title) return null;
  
  const t = title.trim();
  const tLower = t.toLowerCase();

  // 1. Kegiatan DALAM GEDUNG → Lokasi: "Puskesmas"
  if (isInsideBuildingActivity(t)) {
    return "Puskesmas Sangkali";
  }

  // 2. "Pelayanan Posyandu [Nama]" → "Posyandu [Nama]"
  const posyanduMatch = t.match(/^Pelayanan Posyandu\s+(.+)$/i);
  if (posyanduMatch) {
    return `Posyandu ${posyanduMatch[1].trim()}`;
  }

  // 3. "Pelaksanaan Posbindu [Nama]" → "Posbindu [Nama]"
  const posbinduMatch = t.match(/^Pelaksanaan Posbindu\s+(.+)$/i);
  if (posbinduMatch) {
    return `Posbindu ${posbinduMatch[1].trim()}`;
  }

  // 4. "Pelayanan UKK di [Lokasi]" → "[Lokasi]"
  const ukMatch = t.match(/^Pelayanan UKK di\s+(.+)$/i);
  if (ukMatch) {
    return ukMatch[1].trim();
  }

  // 5. "Pelayanan di Pos Yandu Remaja [Nama]" → "Posyandu Remaja [Nama]"
  const remajaMatch = t.match(/^Pelayanan di Pos Yandu Remaja\s+(.+)$/i);
  if (remajaMatch) {
    return `Posyandu Remaja ${remajaMatch[1].trim()}`;
  }

  // 6. "Pelayanan Posyandu" (tanpa nama lokasi) → null (user pilih manual)
  if (tLower === "pelayanan posyandu") {
    return null;
  }

  // 7. "Pelaksanaan Posbindu" (tanpa nama lokasi) → null
  if (tLower === "pelaksanaan posbindu") {
    return null;
  }

  // 8. "Pelayanan UKK" (tanpa lokasi) → null
  if (tLower === "pelayanan ukk") {
    return null;
  }

  // 9. Kegiatan terkait sekolah → null (biarkan user pilih sekolah mana)
  if (
    tLower.includes("di sekolah") ||
    tLower.includes("skring kesehatan di sekolah") ||
    tLower.includes("pembinaan kesehatan di sekolah")
  ) {
    return null;
  }

  // 10. Kegiatan terkait Pustu → "Puskesmas"
  if (tLower.includes("pustu") || tLower.includes("di pustu")) {
    return "Puskesmas Sangkali";
  }

  // Default: null (user pilih manual)
  return null;
}